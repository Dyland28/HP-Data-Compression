WITH COLUMN_MATH AS

(
SELECT
    A.OWNER,
    A.TABLE_NAME,
    SUM(A.AVG_COL_LEN-1) AS AVG_COL_LEN,
    COUNT(*) AS COLUMN_COUNT
FROM ALL_TAB_COLS A
WHERE A.OWNER = 'CAPSTONE'
AND A.TABLE_NAME LIKE 'SIZE_MATH_TEST%'
GROUP BY
    A.OWNER,
    A.TABLE_NAME

)
SELECT DISTINCT
    A.OWNER,
    A.TABLE_NAME,
    A.AVG_COL_LEN,
    B.NUM_ROWS,
    C.BLOCK_SIZE,
    PCT_FREE,
    FLOOR(((100-PCT_FREE)/100*BLOCK_SIZE - :FIXED_HEADER_BYTES -:ITL_BYTES*:ITL_COUNT-:TABLE_COUNT*:TABLE_DIRECTORY_BYTES - :TAIL_SIZE_BYTES)/(2+2+COLUMN_COUNT+A.AVG_COL_LEN+1)) AS MAX_ROWS_PER_BLOCK_NOCOMPRESS
FROM COLUMN_MATH A
    INNER JOIN ALL_TABLES B
        ON A.OWNER = B.OWNER
        AND A.TABLE_NAME = B.TABLE_NAME
    INNER JOIN DBA_TABLESPACES C
        ON B.TABLESPACE_NAME = C.TABLESPACE_NAME
WHERE A.TABLE_NAME LIKE ('SIZE_MATH_TEST%')
ORDER BY
    OWNER,
    TABLE_NAME;
    
